import '@testing-library/jest-dom'

// jsdom doesn't implement matchMedia; tldraw's UserPreferencesManager needs it.
// Guard for node environment tests that also pick up this setup file.
if (typeof window !== 'undefined') {
  Object.defineProperty(window, 'matchMedia', {
    writable: true,
    value: (query: string) => ({
      matches: false,
      media: query,
      onchange: null,
      addListener: () => {},
      removeListener: () => {},
      addEventListener: () => {},
      removeEventListener: () => {},
      dispatchEvent: () => false,
    }),
  })
}
